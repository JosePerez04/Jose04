    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        this.mdlTabla = (DefaultTableModel) jTable9.getModel();
        mdlTabla.removeRow(jTable9.getSelectedRow());
        try{
            String archivo = "Alumnos.txt";
            BufferedWriter bw = new BufferedWriter(new FileWriter(archivo));
            for(int i=0;i<jTable9.getColumnCount();i++){
                for(int j=0;j<jTable9.getColumnCount();j++){
                    bw.write((String)(jTable9.getValueAt(i,j)));
                    if(j<jTable9.getColumnCount()-1){
                        bw.write("|");
                    }
                }
                bw.newLine();
                
            }
            bw.close();
 
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

