    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        mdlTabla = new DefaultTableModel();
        String nombre = jTextField1.getText();
        String Apellidos = jTextField2.getText();
        String Edad = jTextField3.getText();
        String Area = jTextField4.getText();
        String Semestre = jTextField5.getText();
        
        alumno.setNombre(nombre);
        alumno.setApellidos(Apellidos);
        alumno.setEdad(Edad);
        alumno.setArea(Area);
        alumno.setSemestre(Semestre);
        
        metodos.guardar(alumno);
        metodos.guardarArchivo(alumno);
        
        JOptionPane.showMessageDialog(null,"¡Registro exitoso!");
        
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        
        
        
        jTable2.setModel(metodos.listaAlumnos());
        
        
        
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

